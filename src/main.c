#include "ft_ls.h"

int		main(int argc, char **argv)
{
	t_mem	s;
	int		i;

	i = 0;
	parse_arg(argv, &s);
	while (s.files[i] != NULL)
	{
		ls_rec(&s, s.files[i]);
		i++;
	}
	argc = 0;
	return (0);
}
